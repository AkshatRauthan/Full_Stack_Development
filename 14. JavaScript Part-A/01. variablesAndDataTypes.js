// #) Boolean
a = true;
console.log(a)
console.log(typeof a)
console.log("\n")

// #) String
a = "Alan";
console.log(a)
console.log(typeof a)
console.log("\n")

// #) Bigint
a = 123n;
console.log(a)
console.log(typeof a)
console.log("\n")

// #) Undefined
a = undefined;
console.log(a)
console.log(typeof a)
console.log("\n")

// #) Null
a = null;
console.log(a)
console.log(typeof a)
console.log("\n")

// Diffrence Between Null And Undefined : 

// Null : We Place Null Value Intentionally In A Variable To Define That It Do NOt Have Any Value.
// Undefined : Is Placed When The Value We Are Assesing Is Not Assigned Or Do Not Exist.